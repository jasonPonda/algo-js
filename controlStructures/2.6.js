const aNumber = Number(prompt("Enter a number between 1 and 7", ""));

if (aNumber === 1) {
    alert('Monday');
} else if (aNumber === 2) {
    alert('Tuesday');
} else if (aNumber === 3) {
    alert('Wednesday');
} else if (aNumber === 4) {
    alert('Thursday');
} else if (aNumber === 5) {
    alert('Friday');
} else if (aNumber === 6) {
    alert('Saturday');
} else {
    alert('Sunday');
}